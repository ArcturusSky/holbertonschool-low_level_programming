/**
 * op_add - Function that do an addition of "a" and "b"
 * @a: 1st integer
 * @b: 2nd integer
 *
 * Return: a + b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Function that do an substraction of "a" and "b"
 * @a: 1st integer
 * @b: 2nd integer
 *
 * Return: a - b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Function that do a multiplication of "a" and "b"
 * @a: 1st integer
 * @b: 2nd integer
 *
 * Return: a * b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Function that do a division of "a" and "b"
 * @a: 1st integer
 * @b: 2nd integer
 *
 * Return: a / b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Function that returns the remainder of the division
 * @a: 1st integer
 * @b: 2nd integer
 *
 * Return: a % b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
